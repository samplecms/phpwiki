<?php
   //周一到周日
   function week_chatjs($n = "+0")
   {
	   	$timestamp = time()+ ((int)$n*7*86400);
	   	$start =  date('w',$timestamp);
	   	$start = ($timestamp-($start-1)*86400);
	   	return [
	   			strtotime( date('Y-m-d 00:00:00',$start) ),
	   			strtotime( date('Y-m-d 23:59:59',$start) ) + 6 * 86400
	   	];
   }
   
   
   
   
   
   for($i=1;$i<20;$i++){
   		$t = week_chatjs("-$i");
